// 1.Crie um programa que mostre os números de 1 até 100

public class tarefa03ex01 {

    public static void main(String args[]){
        // Feito com a estrutura de repetição "FOR"
        for(int i=1; i<=100; i++) {
            System.out.print(i + " ");
        }
        //PULAR LINHA
        System.out.print("\n");
        // Feito com a estrutura de repetição "WHILE"
        int i=1;
        while(i <= 100) {
            System.out.print(i + " ");
            i++;
        }
        
    }
}